const ApiError = require('../exceptions/api-error');

function normalizeCourseCategory(category) {
    if (category == null || typeof category !== 'string') {
        throw ApiError.BadRequest('Выберите категорию курса');
    }
    const trimmed = category.trim();
    if (!COURSE_CATEGORIES.includes(trimmed)) {
        throw ApiError.BadRequest('Некорректная категория курса');
    }
    return trimmed;
}

